// Forward-declare RPC so Sessions.close() can call it before RPC is declared.
// The real binding happens via the final `api` object — we use a thunk to avoid
// a circular reference at definition time.
let _rpcRef = null;

const Sessions = {
  /**
   * Open a new attendance session using create_attendance_session RPC.
   *
   * @param {{ lecture_hall: string, session_label: string, faculty_id: string, window_seconds: number }} data
   */
  async create(data) {
    try {
      const { data: res, error } = await supabaseClient.rpc('create_attendance_session', {
        p_hall: data.lecture_hall,
        p_session: data.session_label,
        p_faculty_uuid: data.faculty_id,
        p_window_seconds: parseInt(data.window_seconds || '300', 10)
      });
      if (error) return normalizeError(error);
      if (!res.success) return { data: null, error: { message: res.message } };
      return { data: { id: res.session_id, attendance_code: res.attendance_code, end_time: res.end_time }, error: null };
    } catch (err) {
      return normalizeError(err);
    }
  },

  /**
   * @param {string} id — UUID
   */
  async findById(id) {
    return run(
      supabaseClient
        .from('attendance_sessions')
        .select('*, faculty(faculty_name)')
        .eq('id', id)
        .maybeSingle()
    );
  },

  /**
   * Find the most recent ACTIVE session for a given lecture hall.
   * Returns null (not an error) if no active session exists.
   *
   * @param {string} hall — e.g. 'LH-01'
   */
  async findActive(hall) {
    return run(
      supabaseClient
        .from('attendance_sessions')
        .select('*, faculty(faculty_name)')
        .eq('lecture_hall', hall)
        .eq('status', 'active')
        .order('created_at', { ascending: false })
        .limit(1)
        .maybeSingle()
    );
  },

  /**
   * Find any active session for a specific faculty member.
   * Used for session recovery.
   *
   * @param {string} facultyId — UUID
   */
  async findActiveForFaculty(facultyId) {
    return run(
      supabaseClient
        .from('attendance_sessions')
        .select('*')
        .eq('faculty_id', facultyId)
        .eq('status', 'active')
        .order('created_at', { ascending: false })
        .limit(1)
        .maybeSingle()
    );
  },

  /**
   * Return ALL currently active sessions across all halls, with faculty details.
   * Used by the admin dashboard to show a live overview.
   */
  async findAllActive() {
    return run(
      supabaseClient
        .from('attendance_sessions')
        .select('*, faculty(faculty_name)')
        .eq('status', 'active')
        .order('created_at', { ascending: false })
    );
  },

  /**
   * Rotate the 4-digit attendance code for a running session using refresh_attendance_code RPC.
   *
   * @param {string} id — session UUID
   */
  async updateCode(id) {
    try {
      const { data: res, error } = await supabaseClient.rpc('refresh_attendance_code', {
        p_session_id: id
      });
      if (error) return normalizeError(error);
      if (!res.success) return { data: null, error: { message: res.message } };
      return { data: { id, attendance_code: res.attendance_code }, error: null };
    } catch (err) {
      return normalizeError(err);
    }
  },

  /**
   * Close a session and mark all non-attending students as absent.
   *
   * @param {string} id — session UUID
   */
  async close(id) {
    return _rpcRef.closeSession(id);
  },

  /**
   * List all sessions for a calendar date, optionally scoped to a hall.
   *
   * @param {string}      date — ISO date 'YYYY-MM-DD'
   * @param {string|null} hall
   */
  async listByDate(date, hall = null) {
    try {
      let query = supabaseClient
        .from('attendance_sessions')
        .select('*, faculty(faculty_name)')
        .gte('created_at', `${date}T00:00:00.000Z`)
        .lte('created_at', `${date}T23:59:59.999Z`)
        .order('created_at', { ascending: true });

      if (hall) query = query.eq('lecture_hall', hall);

      const { data, error } = await query;
      if (error) return normalizeError(error);
      return { data, error: null };
    } catch (err) {
      return normalizeError(err);
    }
  },

  /**
   * List sessions within a date range, optionally scoped to a hall.
   *
   * @param {string}      from — ISO date 'YYYY-MM-DD'
   * @param {string}      to   — ISO date 'YYYY-MM-DD'
   * @param {string|null} hall
   */
  async listByDateRange(from, to, hall = null) {
    try {
      let query = supabaseClient
        .from('attendance_sessions')
        .select('*, faculty(faculty_name)')
        .gte('created_at', `${from}T00:00:00.000Z`)
        .lte('created_at', `${to}T23:59:59.999Z`)
        .order('created_at', { ascending: true });

      if (hall) query = query.eq('lecture_hall', hall);

      const { data, error } = await query;
      if (error) return normalizeError(error);
      return { data, error: null };
    } catch (err) {
      return normalizeError(err);
    }
  },

  /**
   * Aggregate present / absent / total counts for a closed session.
   *
   * @param {string} sessionId — UUID
   * @returns {Promise<{ data: { present: number, absent: number, total: number }, error: object|null }>}
   */
  async getSessionSummary(sessionId) {
    try {
      const { data, error } = await supabaseClient
        .from('attendance_records')
        .select('status')
        .eq('session_id', sessionId);

      if (error) return normalizeError(error);

      const present = data.filter(r => r.status === 'present').length;
      const absent  = data.filter(r => r.status === 'absent').length;

      return { data: { present, absent, total: present + absent }, error: null };
    } catch (err) {
      return normalizeError(err);
    }
  },
};

