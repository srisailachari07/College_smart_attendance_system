/**
 * api.js — Centralized Supabase Data Access Layer
 *
 * ALL database interactions go through this module.
 * Feature modules (students.js, sessions.js, etc.) import from api.js only.
 * No other JS file calls supabase directly.
 *
 * Each method returns: { data, error } (Supabase standard shape)
 * Errors are caught and normalized — callers receive consistent responses.
 *
 * Usage:
 *   import { api } from './api.js';
 *   const { data, error } = await api.Students.findByMobile('9876543210');
 */

import { supabaseClient } from './supabase-client.js?v=2.2';

// ─────────────────────────────────────────────────────────────────────────────
// Internal helpers
// ─────────────────────────────────────────────────────────────────────────────

/**
 * Normalise any caught exception into the { data, error } contract so every
 * caller can rely on the same shape regardless of what went wrong.
 *
 * @param {unknown} err
 * @returns {{ data: null, error: { message: string, code: string|null, details: unknown } }}
 */
function normalizeError(err) {
  if (err && typeof err === 'object') {
    return {
      data: null,
      error: {
        message: err.message  ?? 'An unexpected error occurred',
        code:    err.code     ?? err.statusCode ?? null,
        details: err.details  ?? err.hint       ?? null,
      },
    };
  }
  return {
    data: null,
    error: { message: String(err), code: null, details: null },
  };
}

/**
 * Wraps a Supabase query promise and normalises the result.
 * Supabase returns { data, error } — we re-use that shape but also catch
 * JS-level throws (network failures, etc.).
 *
 * @param {Promise<{ data: any, error: any }>} queryPromise
 * @returns {Promise<{ data: any, error: any }>}
 */
async function run(queryPromise) {
  try {
    const { data, error, count } = await queryPromise;
    if (error) return normalizeError(error);
    const result = { data, error: null };
    if (count !== undefined && count !== null) {
      result.count = count;
    }
    return result;
  } catch (err) {
    return normalizeError(err);
  }
}

