const Students = {
  /**
   * Find an active student by mobile number.
   * Used during attendance marking to identify who is checking in.
   *
   * @param {string} mobile
   * @returns {Promise<{ data: object|null, error: object|null }>}
   */
  async findByMobile(mobile) {
    return run(
      supabaseClient
        .from('students')
        .select('*')
        .eq('mobile_number', mobile)
        .limit(1)
        .maybeSingle()
    );
  },

  /**
   * Find a student by their internal UUID primary key.
   *
   * @param {string} id — UUID
   */
  async findById(id) {
    return run(
      supabaseClient
        .from('students')
        .select('*')
        .eq('id', id)
        .maybeSingle()
    );
  },

  /**
   * Find a student by the business-key student_id (e.g. "STU000042").
   * student_id is generated by a PostgreSQL SEQUENCE in the DB.
   *
   * @param {string} studentId
   */
  async findByStudentId(studentId) {
    return run(
      supabaseClient
        .from('students')
        .select('*')
        .eq('student_id', studentId)
        .maybeSingle()
    );
  },

  /**
   * Find an active student by their University Roll Number.
   *
   * @param {string} rollNumber
   */
  async findByRollNumber(rollNumber) {
    return run(
      supabaseClient
        .from('students')
        .select('*')
        .eq('roll_number', rollNumber)
        .maybeSingle()
    );
  },

  /**
   * Create a new student record using register_student RPC.
   *
   * @param {{ student_name: string, mobile_number: string, lecture_hall: string, roll_number: string }} data
   */
  async create(data) {
    try {
      const { data: res, error } = await supabaseClient.rpc('register_student', {
        p_name: data.student_name,
        p_mobile: data.mobile_number,
        p_hall: data.lecture_hall,
        p_roll: data.roll_number || ''
      });
      if (error) return normalizeError(error);
      if (!res.success) return { data: null, error: { message: res.message } };
      return { data: res.student, error: null };
    } catch (err) {
      return normalizeError(err);
    }
  },

  /**
   * Update mutable fields on an existing student record.
   *
   * @param {string} id — UUID
   * @param {object} data — partial fields to update
   */
  async update(id, data) {
    return run(
      supabaseClient
        .from('students')
        .update(data)
        .eq('id', id)
        .select()
        .single()
    );
  },

  /**
   * Soft-deactivate student account: sets is_active = false.
   *
   * @param {string} id — UUID
   */
  async deactivate(id) {
    return run(
      supabaseClient
        .from('students')
        .update({ is_active: false })
        .eq('id', id)
        .select()
        .single()
    );
  },

  /**
   * Soft-activate student account: sets is_active = true.
   *
   * @param {string} id — UUID
   */
  async activate(id) {
    return run(
      supabaseClient
        .from('students')
        .update({ is_active: true })
        .eq('id', id)
        .select()
        .single()
    );
  },

  /**
   * Check whether a mobile number is already in use.
   * Pass excludeId to allow the current student's own number through during updates.
   *
   * @param {string} mobile
   * @param {string|null} excludeId — UUID of the student to skip
   * @returns {Promise<{ data: boolean, error: object|null }>} data=true means duplicate exists
   */
  async checkMobileDuplicate(mobile, excludeId = null) {
    try {
      let query = supabaseClient
        .from('students')
        .select('id', { count: 'exact', head: true })
        .eq('mobile_number', mobile);

      if (excludeId) {
        query = query.neq('id', excludeId);
      }

      const { count, error } = await query;
      if (error) return normalizeError(error);
      return { data: count > 0, error: null };
    } catch (err) {
      return normalizeError(err);
    }
  },

  /**
   * Paginated, filterable student list.
   *
   * @param {{ hall?: string, search?: string, isActive?: boolean, page?: number, limit?: number }} options
   * @returns {Promise<{ data: object[], error: object|null }>}
   */
  async list({ hall, search, isActive, page = 1, limit = 50 } = {}) {
    try {
      let query = supabaseClient
        .from('students')
        .select('*')
        .order('student_name', { ascending: true });

      if (hall     !== undefined && hall     !== null) query = query.eq('lecture_hall', hall);
      if (isActive !== undefined && isActive !== null) query = query.eq('is_active', isActive);
      if (search) {
        query = query.or(
          `student_name.ilike.%${search}%,student_id.ilike.%${search}%,mobile_number.ilike.%${search}%,roll_number.ilike.%${search}%`
        );
      }

      const from = (page - 1) * limit;
      const to   = from + limit - 1;
      query = query.range(from, to);

      const { data, error } = await query;
      if (error) return normalizeError(error);
      return { data, error: null };
    } catch (err) {
      return normalizeError(err);
    }
  },

  /**
   * Count students, optionally filtered by hall and/or active status.
   *
   * @param {{ hall?: string, isActive?: boolean }} options
   * @returns {Promise<{ data: number, error: object|null }>}
   */
  async count({ hall, isActive } = {}) {
    try {
      let query = supabaseClient
        .from('students')
        .select('*', { count: 'exact', head: true });

      if (hall     !== undefined && hall     !== null) query = query.eq('lecture_hall', hall);
      if (isActive !== undefined && isActive !== null) query = query.eq('is_active', isActive);

      const { count, error } = await query;
      if (error) return normalizeError(error);
      return { data: count, error: null };
    } catch (err) {
      return normalizeError(err);
    }
  },

  /**
   * Count students whose overall attendance percentage is below the given threshold.
   * Joins student_attendance_summary to get the percentage.
   *
   * @param {number} threshold — e.g. 75
   * @returns {Promise<{ data: number, error: object|null }>}
   */
  async countBelowThreshold(threshold) {
    try {
      const { count, error } = await supabaseClient
        .from('attendance_summary')
        .select('*', { count: 'exact', head: true })
        .lt('attendance_percentage', threshold);

      if (error) return normalizeError(error);
      return { data: count, error: null };
    } catch (err) {
      return normalizeError(err);
    }
  },

  /**
   * Delete a student profile permanently (cascades to summary & records).
   *
   * @param {string} id — Student UUID
   */
  async delete(id) {
    return run(
      supabaseClient
        .from('students')
        .delete()
        .eq('id', id)
        .select()
        .single()
    );
  },

  /**
   * Get all student IDs and mobile numbers to validate bulk imports efficiently.
   */
  async getAllStudentIdsAndMobiles() {
    return run(
      supabaseClient
        .from('students')
        .select('roll_number, mobile_number')
    );
  },

  /**
   * Delete multiple students in a single query.
   *
   * @param {Array<string>} idsArray
   */
  async deleteMultiple(idsArray) {
    return run(
      supabaseClient
        .from('students')
        .delete()
        .in('id', idsArray)
    );
  },

  /**
   * Reset all student profiles in the database.
   */
  async resetAll() {
    try {
      const { data: res, error } = await supabaseClient.rpc('reset_all_students');
      if (error) return normalizeError(error);
      return { data: true, error: null };
    } catch (err) {
      return normalizeError(err);
    }
  },
};

