const Summary = {
  /**
   * Get the attendance summary row for a student.
   *
   * @param {string} studentId — UUID
   */
  async findByStudent(studentId) {
    return run(
      supabaseClient
        .from('attendance_summary')
        .select('*')
        .eq('student_id', studentId)
        .maybeSingle()
    );
  },

  /**
   * Ensure a summary row exists for a student.
   * Inserts with 0/0/0 if not present; a no-op if it already exists.
   *
   * @param {string} studentId — UUID
   */
  async ensure(studentId) {
    return run(
      supabaseClient
        .from('attendance_summary')
        .upsert(
          {
            student_id:            studentId,
            present_count:         0,
            absent_count:          0,
          },
          { onConflict: 'student_id', ignoreDuplicates: true }
        )
        .select()
        .single()
    );
  },

  /**
   * Return summary rows joined with student details for an entire lecture hall.
   *
   * @param {string} hall — e.g. 'LH-03'
   */
  async byHall(hall) {
    return run(
      supabaseClient
        .from('attendance_summary')
        .select('*, students!inner(student_id, student_name, roll_number, mobile_number, lecture_hall, is_active)')
        .eq('students.lecture_hall', hall)
        .eq('students.is_active', true)
        .order('students.student_name', { ascending: true })
    );
  },

  /**
   * Paginated summary with optional hall and percentage-range filters.
   *
   * @param {{ hall?: string, minPct?: number, maxPct?: number, page?: number, limit?: number }} options
   */
  async all({ hall, minPct, maxPct, page = 1, limit = 50 } = {}) {
    try {
      let query = supabaseClient
        .from('attendance_summary')
        .select('*, students!inner(student_id, student_name, roll_number, lecture_hall, is_active)')
        .eq('students.is_active', true)
        .order('attendance_percentage', { ascending: true });

      if (hall   !== undefined && hall   !== null) query = query.eq('students.lecture_hall', hall);
      if (minPct !== undefined && minPct !== null) query = query.gte('attendance_percentage', minPct);
      if (maxPct !== undefined && maxPct !== null) query = query.lte('attendance_percentage', maxPct);

      const from = (page - 1) * limit;
      const to   = from + limit - 1;
      query = query.range(from, to);

      const { data, error } = await query;
      if (error) return normalizeError(error);
      return { data, error: null };
    } catch (err) {
      return normalizeError(err);
    }
  },

  /**
   * Students below the low-attendance threshold, optionally scoped to a hall.
   *
   * @param {number}      threshold — percentage, e.g. 75
   * @param {string|null} hall
   */
  async belowThreshold(threshold, hall = null) {
    try {
      let query = supabaseClient
        .from('attendance_summary')
        .select('*, students!inner(student_id, student_name, roll_number, mobile_number, lecture_hall, is_active)')
        .lt('attendance_percentage', threshold)
        .eq('students.is_active', true)
        .order('attendance_percentage', { ascending: true });

      if (hall) query = query.eq('students.lecture_hall', hall);

      const { data, error } = await query;
      if (error) return normalizeError(error);
      return { data, error: null };
    } catch (err) {
      return normalizeError(err);
    }
  },
};

